class AquariumAnimal():
    def __init__(self, name="", age=0, year_in_zoo=0, sex=None,
                    volume_of_aquarium=0, Species=None):
        self.name = name
        self.age = age
        self.year_in_zoo = year_in_zoo
        self.Sex = sex
        self.volume_of_aquarium = volume_of_aquarium
        self.Species = Species
