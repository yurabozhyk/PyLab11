class AquariumAnimal():
    def __init__(self, name="", age=0, yearInZoo=0, sex=None,
                    volumeOfAquarium=0, Species=None):
        self.name = name
        self.age = age
        self.yearInZoo = yearInZoo
        self.Sex = sex
        self.volumeOfAquarium = volumeOfAquarium
        self.Species = Species
